# =============================================================================
# Token acquisition — JWT Bearer Grant (RFC 7523)
#
# Industry-standard approach for service-account auth against OIDC providers
# that don't support ROPC (Zitadel, Okta, Azure AD all dropped it).
#
# Flow:
#   1. Load the E2E SA JSON key (generated by Terraform, written by start.sh)
#   2. Build a short-lived JWT assertion signed with the SA's RSA private key
#   3. POST to /oauth/v2/token with grant_type=jwt-bearer
#   4. Receive a proper access JWT the gateway validates normally
#
# Key file: config/zitadel/e2e-sa-key.json  (auto-generated, git-ignored)
# Format (Zitadel machine user key):
#   { "type": "serviceaccount", "keyId": "...", "key": "-----BEGIN RSA PRIVATE KEY-----...",
#     "appId": "...", "clientId": "user@<instance>.zitadel.cloud" }
# =============================================================================

import uuid as _uuid
from datetime import datetime, timedelta, timezone


def _load_key_file() -> dict:
    """Load the E2E SA key from file path in env or default location."""
    project_root = os.path.realpath(os.path.join(os.path.dirname(os.path.abspath(__file__)), "../.."))
    key_file = os.environ.get("E2E_SA_KEY_FILE") or "config/zitadel/e2e-sa-key.json"
    # Relative paths are anchored to the project root, not the script's CWD
    if not os.path.isabs(key_file):
        key_file = os.path.join(project_root, key_file)
    key_path = os.path.realpath(key_file)
    if not os.path.exists(key_path):
        print(
            f"\nERROR: E2E service account key not found at:\n  {key_path}\n\n"
            "Run ./scripts/docintel.sh → Start to (re)generate it.\n"
            "The key is written to config/zitadel/e2e-sa-key.json by start.sh.\n",
            file=sys.stderr,
        )
        sys.exit(1)
    with open(key_path) as f:
        return json.load(f)


def acquire_token(
    auth_cfg: dict,
    username: str | None,        # unused — SA auth needs no username
    password: str | None,        # unused
    client_id: str | None = None,
) -> tuple[str | None, int]:
    """
    Acquire a bearer JWT via JWT Bearer Grant (RFC 7523).
    Returns None only if auth_cfg has no token_url (i.e. auth fully disabled).
    """
    if not auth_cfg.get("token_url"):
        return None, 0

    try:
        import jwt as _jwt                    # PyJWT[crypto]
    except ImportError:
        print(
            "ERROR: PyJWT not installed. Run:\n"
            "  pip install -r tests/integration/requirements.txt\n",
            file=sys.stderr,
        )
        sys.exit(1)

    token_url    = auth_cfg["token_url"]
    zitadel_base = "/".join(token_url.split("/")[:3])

    key_data = _load_key_file()
    private_key = key_data["key"]
    key_id      = key_data["keyId"]
    # Machine user keys use userId as issuer/subject (not clientId — that's for app keys)
    user_id_claim = key_data["userId"]

    now = datetime.now(timezone.utc)
    assertion = _jwt.encode(
        {
            "iss": user_id_claim,
            "sub": user_id_claim,
            "aud": [zitadel_base],   # Zitadel validates aud = issuer (base URL, not token URL)
            "iat": now,
            "exp": now + timedelta(minutes=5),
            "jti": str(_uuid.uuid4()),
        },
        private_key,
        algorithm="RS256",
        headers={"kid": key_id},
    )

    print("Acquiring token via JWT Bearer grant (RFC 7523) …")
    try:
        resp = httpx.post(
            token_url,
            data={
                "grant_type": "urn:ietf:params:oauth:grant-type:jwt-bearer",
                "scope":      auth_cfg.get("scopes", "openid email profile"),
                "assertion":  assertion,
            },
            timeout=30,
        )
        resp.raise_for_status()
        body = resp.json()
        token = body.get("access_token")
        if not token:
            raise ValueError(f"No access_token in response: {resp.text[:200]}")
        expires_in = int(body.get("expires_in", 899))
        print(f"Token acquired (expires_in={expires_in}s)\n")
        return token, expires_in
    except Exception as exc:
        print(f"ERROR: Failed to acquire token — {exc}", file=sys.stderr)
        sys.exit(1)


