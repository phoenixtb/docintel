import hashlib as _hashlib
import hmac as _hmac


def _load_internal_secret() -> str:
    """Load INTERNAL_GATEWAY_SECRET from .env relative to the project root."""
    project_root = os.path.realpath(os.path.join(os.path.dirname(os.path.abspath(__file__)), "../.."))
    env_file = os.path.join(project_root, ".env")
    if not os.path.exists(env_file):
        # Fall back to environment variable (e.g. when sourced externally)
        secret = os.environ.get("INTERNAL_GATEWAY_SECRET", "")
        if secret:
            return secret
        print(
            "ERROR: INTERNAL_GATEWAY_SECRET not found.\n"
            f"  Expected .env at: {env_file}\n"
            "  Or set INTERNAL_GATEWAY_SECRET environment variable.\n",
            file=sys.stderr,
        )
        sys.exit(1)
    with open(env_file) as f:
        for line in f:
            line = line.strip()
            if line.startswith("INTERNAL_GATEWAY_SECRET="):
                return line.split("=", 1)[1].strip().strip('"').strip("'")
    secret = os.environ.get("INTERNAL_GATEWAY_SECRET", "")
    if not secret:
        print("ERROR: INTERNAL_GATEWAY_SECRET not set in .env or environment.\n", file=sys.stderr)
        sys.exit(1)
    return secret


def _hmac_token(request_id: str, tenant_id: str, user_id: str, secret: str) -> str:
    msg = f"{request_id}:{tenant_id}:{user_id}".encode()
    return _hmac.new(secret.encode(), msg, _hmac.new.__class__.__mro__[-1]).hexdigest()  # type: ignore[attr-defined]


def _compute_hmac(request_id: str, tenant_id: str, user_id: str, secret: str) -> str:
    msg = f"{request_id}:{tenant_id}:{user_id}".encode()
    return _hmac.new(secret.encode(), msg, _hashlib.sha256).hexdigest()


def _ensure_tenant_exists(base_url: str, tenant_id: str, bearer_token: str) -> None:
    """
    Register the tenant in the admin service if it doesn't already exist.
    Idempotent — 409 Conflict is silently accepted.
    """
    headers = {
        "Authorization": f"Bearer {bearer_token}",
        "X-Tenant-Id": tenant_id,
        "Content-Type": "application/json",
    }
    resp = httpx.post(
        f"{base_url.rstrip('/')}/api/v1/tenants",
        headers=headers,
        json={"id": tenant_id, "name": "E2E Test Tenant"},
        timeout=15,
    )
    if resp.status_code in (200, 201):
        print(f"  ✓ Tenant '{tenant_id}' registered in admin service.")
    elif resp.status_code == 409:
        print(f"  ✓ Tenant '{tenant_id}' already exists.")
    else:
        print(
            f"  ERROR: Failed to register tenant '{tenant_id}': HTTP {resp.status_code}: {resp.text[:200]}",
            file=sys.stderr,
        )
        sys.exit(1)


def setup_e2e_environment(
    tenant_id: str,
    base_url: str = "http://localhost:8080",
    ingestion_url: str = "http://localhost:8001",
    data_loader_url: str = "http://localhost:8003",
    bearer_token: str | None = None,
) -> None:
    """
    Seed test datasets into the e2e tenant via the data-loader service.

    Dataset loading moved from ingestion-service (/ingest/dataset/*) to
    data-loader (/datasets/*) as part of the Phase 1 pipeline redesign.
    The data-loader auth uses X-User-Id (not the HMAC service token).

    Seeds all three suites' datasets so every query suite has documents.
    The 'e2e' tenant is pre-seeded in the DB (config/postgres/init.sql).
    Blocks until the ingestion job completes or raises on error.
    """
    user_id = "e2e-test-runner"

    print("Setup: seeding test datasets into e2e tenant …")
    try:
        resp = httpx.post(
            f"{data_loader_url}/datasets/load",
            headers={
                "Content-Type": "application/json",
                "X-User-Id": user_id,
                "X-Tenant-Id": tenant_id,
            },
            json={"datasets": ["techqa", "hr_policies", "cuad"], "samples_per_dataset": 5},
            timeout=30,
        )
        resp.raise_for_status()
    except Exception as exc:
        print(
            f"ERROR: Failed to start seed job — {exc}\n"
            f"  Is data-loader reachable at {data_loader_url}?\n",
            file=sys.stderr,
        )
        sys.exit(1)

    job_id = resp.json().get("message", "")
    if not job_id:
        print(f"ERROR: Could not parse job_id from seed response: {resp.text[:200]}", file=sys.stderr)
        sys.exit(1)
    print(f"  Seed job started (job_id={job_id})")

    # Stream SSE progress from data-loader until done/error
    success = False
    with httpx.Client(timeout=600) as client:
        with client.stream(
            "GET",
            f"{data_loader_url}/datasets/load/{job_id}/progress",
            headers={
                "X-User-Id": user_id,
                "X-Tenant-Id": tenant_id,
            },
        ) as stream:
            total_docs = 0
            done_docs = 0
            current_event_type = None
            for line in stream.iter_lines():
                # SSE format: "event: {type}" then "data: {json}" then blank line
                if line.startswith("event: "):
                    current_event_type = line[7:].strip()
                    continue
                if not line.startswith("data: "):
                    continue
                try:
                    event = json.loads(line[6:])
                except json.JSONDecodeError:
                    continue
                # Type is from the SSE "event:" field; fall back to JSON body "type" for compat
                etype = current_event_type or event.get("type") or event.get("event")
                current_event_type = None  # reset for next event
                if etype == "total":
                    total_docs = event.get("total", 0)
                elif etype == "progress":
                    done_docs = event.get("processed", done_docs + 1)
                    pct = int(done_docs / total_docs * 100) if total_docs else 0
                    print(f"\r  Ingesting … {done_docs}/{total_docs} ({pct}%)", end="", flush=True)
                elif etype == "done":
                    done_docs = event.get("processed", total_docs)
                    print(f"\r  ✓ Ingested {done_docs} documents into tenant '{tenant_id}'")
                    success = True
                    return
                elif etype == "error":
                    reason = event.get("reason", event)
                    print(f"\nERROR: Seed job failed: {reason}", file=sys.stderr)
                    sys.exit(1)

    if not success:
        print(
            "\nERROR: Seed job stream closed without a 'done' event.\n"
            "  The ingestion job likely failed — check ingestion-service logs.\n",
            file=sys.stderr,
        )
        sys.exit(1)


def teardown_e2e_environment(
    base_url: str,
    tenant_id: str,
    bearer_token: str | None,
) -> None:
    """
    Delete all documents from the e2e tenant by listing then deleting each one.
    Uses the standard authenticated gateway endpoints — no HMAC needed.
    Always called (try/finally) to prevent artifact accumulation.
    """
    print(f"\nTeardown: deleting all documents for tenant '{tenant_id}' …")
    headers: dict[str, str] = {"X-Tenant-Id": tenant_id}
    if bearer_token:
        headers["Authorization"] = f"Bearer {bearer_token}"

    base = base_url.rstrip("/")
    deleted = 0
    errors = 0
    try:
        # Paginate until no more documents remain
        while True:
            resp = httpx.get(
                f"{base}/api/v1/documents",
                headers=headers,
                params={"page": 0, "size": 50},
                timeout=30,
            )
            if resp.status_code != 200:
                print(
                    f"  ⚠  GET /api/v1/documents returned HTTP {resp.status_code} — skipping cleanup.",
                    file=sys.stderr,
                )
                return

            data = resp.json()
            # Spring Page response: {"content": [...], "totalElements": N}
            docs = data.get("content", data if isinstance(data, list) else [])
            if not docs:
                break

            for doc in docs:
                doc_id = doc.get("id") or doc.get("documentId")
                if not doc_id:
                    continue
                del_resp = httpx.delete(
                    f"{base}/api/v1/documents/{doc_id}",
                    headers=headers,
                    timeout=30,
                )
                if del_resp.status_code in (200, 204, 404):
                    deleted += 1
                else:
                    errors += 1

        if errors:
            print(f"  ⚠  Deleted {deleted} docs, {errors} errors. Manual cleanup may be required.")
        else:
            print(f"  ✓ Tenant '{tenant_id}' cleaned up ({deleted} documents deleted).")
    except Exception as exc:
        print(f"  ⚠  Teardown failed (non-fatal): {exc}", file=sys.stderr)


