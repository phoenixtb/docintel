class PreFlightError(SystemExit):
    pass


def _check_url(label: str, url: str, timeout: int = 8) -> None:
    try:
        r = httpx.get(url, timeout=timeout, follow_redirects=True)
        if r.status_code >= 500:
            print(
                f"\nERROR: {label} returned HTTP {r.status_code}.\n"
                f"  URL: {url}\n"
                f"  The service may be starting up or misconfigured.\n",
                file=sys.stderr,
            )
            raise PreFlightError(2)
    except httpx.ConnectError:
        print(
            f"\nERROR: Cannot reach {label}.\n"
            f"  URL: {url}\n"
            f"  Is the DocIntel stack running?  ./scripts/docintel.sh → Start\n",
            file=sys.stderr,
        )
        raise PreFlightError(2)
    except httpx.TimeoutException:
        print(
            f"\nERROR: {label} timed out after {timeout}s.\n"
            f"  URL: {url}\n"
            f"  Service may be overloaded or not yet healthy.\n",
            file=sys.stderr,
        )
        raise PreFlightError(2)


def pre_flight(base_url: str, rag_path: str, no_auth: bool, token_url: str | None, tenant_id: str) -> None:
    """Fail fast with a clear message if the stack or Zitadel is unreachable."""
    print("Pre-flight checks …")

    # API Gateway health — always the entry point (rag-service port is not exposed to host)
    gw_health = base_url.rstrip("/") + "/actuator/health"
    _check_url("API Gateway", gw_health)
    print(f"  ✓ API Gateway reachable ({base_url})")

    # Zitadel — only needed when auth is enabled
    if not no_auth and token_url:
        zitadel_base = "/".join(token_url.split("/")[:3])  # http://host:port
        _check_url("Zitadel", zitadel_base + "/debug/healthz")
        print(f"  ✓ Zitadel reachable ({zitadel_base})")

    print()


